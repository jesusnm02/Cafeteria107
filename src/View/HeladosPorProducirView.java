    public void mostrarHeladosProducibles(Map<String, Integer> heladosProducibles) {
        // Crear un modelo para la tabla
        DefaultTableModel modelo = new DefaultTableModel();

        // Agregar las columnas al modelo
        modelo.addColumn("Nombre del Helado");
        modelo.addColumn("Cantidad Producible");

        // Agregar las filas con los datos del mapa
        for (Map.Entry<String, Integer> entry : heladosProducibles.entrySet()) {
            modelo.addRow(new Object[]{entry.getKey(), entry.getValue()});
        }
        TableHeladosPorProducir.setModel(modelo);
    }
