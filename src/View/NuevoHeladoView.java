    public void llenarComboBoxcategoria(Collection<CategoriaDTO> ListaCategoria) {
        comboBoxCategoria.removeAllItems();
        for(CategoriaDTO categoria: ListaCategoria) {
            comboBoxCategoria.addItem(categoria.getNomCategoria());
        }
    }
    
    public void llenarComboBoxAlmacen(Collection<AlmacenDTO> ListaAlmacen) {
        comboBoxAlmacen.removeAllItems();
        for(AlmacenDTO almacen: ListaAlmacen) {
            comboBoxAlmacen.addItem(almacen.getNomAlmacen());
        }
    }
    
